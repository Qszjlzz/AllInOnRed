export function getCopy(path, ...args) {
  const parts = path.split('.');
  let cur = COPY;
  for (const part of parts) {
    cur = cur?.[part];
    if (cur === undefined) return path;
  }
  return typeof cur === 'function' ? cur(...args) : cur;
}
